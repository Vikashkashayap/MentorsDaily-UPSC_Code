export function DynIcon({ name, className, size = 18, strokeWidth, ...rest }) {
  const I = ICON_MAP[name];
  if (!I) return null;
  return (
    <I
      className={className}
      size={size}
      {...(strokeWidth != null ? { strokeWidth } : {})}
      {...rest}
    />
  );
}
